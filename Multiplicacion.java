public class Multiplicacion {

    /* declaramos el tipo de variable */
    public static void main(String args[]){
        double numero_1 = 300.45;
        double numero_2 = 400.56;
        double numero_3 = 23.3;
        double result = 0;

        /* concatenamos el resultado que queremos que sea de vuelto */
        result = numero_1 * numero_2 * numero_3;

        /* Imprimos en panatalla */
        System.out.println("El resultado para esta expresion es : " + result);
    }
}
